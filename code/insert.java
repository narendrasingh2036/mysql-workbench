    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/narendra","root","narendra");
            String str=null;
            Statement stmt=con.createStatement();
            String a=columnno.getText();
            if(a.equals("1"))
                str="insert into "+tablename.getText()+" values('"+entry1.getText()+"');";
            else if(a.equals("2"))
                str="insert into "+tablename.getText()+" values('"+entry1.getText()+"','"+entry2.getText()+"');";
            else if(a.equals("3"))
                str="insert into "+tablename.getText()+" values('"+entry1.getText()+"','"+entry2.getText()+"','"+entry3.getText()+"');";
            else if(a.equals("4"))
                str="insert into "+tablename.getText()+" values('"+entry1.getText()+"','"+entry2.getText()+"','"+entry3.getText()+"','"+entry4.getText()+"');";
            else
                JOptionPane.showMessageDialog(null, "Enter no of columns");
            stmt.execute(str);
            JOptionPane.showMessageDialog(null, "Insert successfully done");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in inserting");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

